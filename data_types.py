# Data Types

# Python has several built-in data types, including numbers, strings, and booleans.

# Numbers can be integers or floating point values.
a = 10
b = 3.14

# Strings are sequences of characters, and can be enclosed in single or double quotes.
c = "Hello, world!"

# Booleans represent True or False values.
d = True
e = False

# You can use the type function to check the data type of a value.
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
