# Control Structures

# Control structures are used to control the flow of execution of a program.
# Python has several built-in control structures, including if-else statements, which are used to execute a block of code based on a condition.

x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is not greater than 5")

# For loops are used to iterate over a sequence of values.
# The range function generates a sequence of numbers.
for i in range(5):
    print(i)

# You can also use for loops to iterate over a list.
